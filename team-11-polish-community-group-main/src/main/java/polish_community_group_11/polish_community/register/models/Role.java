package polish_community_group_11.polish_community.register.models;

public class Role {
    private int id;
    private String name;  // Role name

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
